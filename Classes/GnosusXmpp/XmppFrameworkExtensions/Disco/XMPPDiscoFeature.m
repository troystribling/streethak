#import "XMPPDiscoFeature.h"

//-----------------------------------------------------------------------------------------------------------------------------------

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation XMPPDiscoFeature

//===================================================================================================================================
#pragma mark XMPPDiscoFeature

//-----------------------------------------------------------------------------------------------------------------------------------

//===================================================================================================================================
#pragma mark XMPPDiscoFeature

//-----------------------------------------------------------------------------------------------------------------------------------
+ (XMPPDiscoFeature*)createFromElement:(NSXMLElement*)element {
	XMPPDiscoFeature* result = (XMPPDiscoFeature*)element;
	result->isa = [XMPPDiscoFeature class];
	return result;
}

//-----------------------------------------------------------------------------------------------------------------------------------
- (XMPPDiscoFeature*)initWithVar:(NSString*)featVar {
	if(self = [super initWithName:@"feature"]) {
        [self addVar:featVar];
	}
	return self;
}

//-----------------------------------------------------------------------------------------------------------------------------------
- (NSString*)var {
    return [[self attributeForName:@"var"] stringValue];
}

//-----------------------------------------------------------------------------------------------------------------------------------
- (void)addVar:(NSString*)val {
    [self addAttributeWithName:@"var" stringValue:val];
}

//===================================================================================================================================
#pragma mark NSObject

//-----------------------------------------------------------------------------------------------------------------------------------
- (void)dealloc {
    [super dealloc];
}

@end
