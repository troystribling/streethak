#import "XMPPQuery.h"
#import "NSXMLElementAdditions.h"

//-----------------------------------------------------------------------------------------------------------------------------------

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation XMPPQuery

//-----------------------------------------------------------------------------------------------------------------------------------

//===================================================================================================================================
#pragma mark XMPPQuery

//-----------------------------------------------------------------------------------------------------------------------------------
+ (XMPPQuery*)createFromElement:(NSXMLElement*)element {
	XMPPQuery* result = (XMPPQuery*)element;
	result->isa = [XMPPQuery class];
	return result;
}

//-----------------------------------------------------------------------------------------------------------------------------------
- (XMPPQuery*)initWithXMLNS:(NSString*)xmlnsVal {
	if(self = [super initWithName:@"query"]) {
        [self addNamespace:[NSXMLNode namespaceWithName:@"" stringValue:xmlnsVal]];
	}
	return self;
}

//-----------------------------------------------------------------------------------------------------------------------------------
- (NSString*)node {
    return [[self attributeForName:@"node"] stringValue];
}

//-----------------------------------------------------------------------------------------------------------------------------------
- (void)addNode:(NSString*)val {
    [self addAttributeWithName:@"node" stringValue:val];
}

//===================================================================================================================================
#pragma mark NSObject

//-----------------------------------------------------------------------------------------------------------------------------------
- (void)dealloc {
    [super dealloc];
}

@end
