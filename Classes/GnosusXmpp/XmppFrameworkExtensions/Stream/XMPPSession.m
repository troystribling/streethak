#import "XMPPSession.h"

//-----------------------------------------------------------------------------------------------------------------------------------

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation XMPPSession

//-----------------------------------------------------------------------------------------------------------------------------------

//===================================================================================================================================
#pragma mark XMPPSession

//-----------------------------------------------------------------------------------------------------------------------------------
+ (XMPPSession*)createFromElement:(NSXMLElement*)element {
	XMPPSession* result = (XMPPSession*)element;
	result->isa = [XMPPSession class];
	return result;
}

//-----------------------------------------------------------------------------------------------------------------------------------
- (XMPPSession*)init {
	if(self = [super initWithName:@"session"]) {
        [self addNamespace:[NSXMLNode namespaceWithName:@"" stringValue:@"urn:ietf:params:xml:ns:xmpp-session"]];
	}
	return self;
}

//===================================================================================================================================
#pragma mark NSObject

//-----------------------------------------------------------------------------------------------------------------------------------
- (void)dealloc {
    [super dealloc];
}

@end
